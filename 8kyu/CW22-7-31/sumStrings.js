//Create a function that takes 2 integers in form of a string as an input, and outputs the sum (also as a string)

//
//Pr: a, b - two strings containing number

//Rt: string of the total

//Ex: sumStr("34","5") returns "39"

function sumStr(a,b) {
    //Convert a and b number total to a string
    return String(Number(a)+Number(b));
  }

